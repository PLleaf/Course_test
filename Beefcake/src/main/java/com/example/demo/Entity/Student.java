package com.sample;


public class Student {

  private String id;
  private String name;
  private long sex;
  private long inforInterval;
  private String email;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public long getSex() {
    return sex;
  }

  public void setSex(long sex) {
    this.sex = sex;
  }


  public long getInforInterval() {
    return inforInterval;
  }

  public void setInforInterval(long inforInterval) {
    this.inforInterval = inforInterval;
  }


  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }

}
