package com.sample;


public class Seminar {

  private long id;
  private long roundNo;
  private long status;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getRoundNo() {
    return roundNo;
  }

  public void setRoundNo(long roundNo) {
    this.roundNo = roundNo;
  }


  public long getStatus() {
    return status;
  }

  public void setStatus(long status) {
    this.status = status;
  }

}
