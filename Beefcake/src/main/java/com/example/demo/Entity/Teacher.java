package com.sample;


public class Teacher {

  private String id;
  private String name;
  private long sex;
  private String email;
  private String title;
  private long inforInterval;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public long getSex() {
    return sex;
  }

  public void setSex(long sex) {
    this.sex = sex;
  }


  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }


  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }


  public long getInforInterval() {
    return inforInterval;
  }

  public void setInforInterval(long inforInterval) {
    this.inforInterval = inforInterval;
  }

}
